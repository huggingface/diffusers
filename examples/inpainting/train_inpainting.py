def log_validation(
    validation_dataloader,
    vae,
    text_encoder,
    tokenizer,
    unet,
    args,
    accelerator,
    weight_dtype,
    epoch,
    is_final_validation=False,
):
    logger.info("Running validation... ")

    pipeline = StableDiffusionInpaintPipeline.from_pretrained(
        args.pretrained_model_name_or_path,
        vae=accelerator.unwrap_model(vae),
        text_encoder=accelerator.unwrap_model(text_encoder),
        tokenizer=tokenizer,
        unet=accelerator.unwrap_model(unet),
        safety_checker=None,
        revision=args.revision,
        variant=args.variant,
        torch_dtype=weight_dtype,
    )

    pipeline = pipeline.to(accelerator.device)
    pipeline.set_progress_bar_config(disable=True)

    if args.enable_xformers_memory_efficient_attention:
        pipeline.enable_xformers_memory_efficient_attention()

    if args.seed is None:
        generator = None
    else:
        generator = torch.Generator(device=accelerator.device).manual_seed(args.seed)

    images = []
    prompts = []
    masks = []
    image_transform = transforms.ToPILImage()
    for _, batch in enumerate(validation_dataloader):
        mask = image_transform(batch["masks"][0] * 255)
        init_image = image_transform(batch["pixel_values"][0])
        prompt = batch["prompts"][0]

        with torch.autocast("cuda"):
            image = pipeline(
                prompt, image=init_image, mask_image=mask, num_inference_steps=20, generator=generator
            ).images[0]

        prompts.append(prompt)
        images.append(image)
        masks.append(mask)

    for tracker in accelerator.trackers:
        phase_name = "test" if is_final_validation else "validation"
        if tracker.name == "tensorboard":
            np_images = np.stack([np.asarray(img) for img in images])
            tracker.writer.add_images(phase_name, np_images, epoch, dataformats="NHWC")
        elif tracker.name == "wandb":
            tracker.log(
                {
                    phase_name: [
                        wandb.Image(make_image_grid([image, masks[i]], 1, 2), caption=f"{i}: {prompts[i]}")
                        for i, image in enumerate(images)
                    ]
                }
            )
        else:
            logger.warn(f"image logging not implemented for {tracker.name}")

    del pipeline
    torch.cuda.empty_cache()

    return images, prompts, masks


